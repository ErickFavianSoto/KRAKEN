<h1 id=mision><strong>Misión:</strong></h1><br/><br/>
<h2>Kraken es una aplicación enfocada a la sistematización de intercambios, en el entorno estudiantil y con la temática de intercambiar videojuegos, la cual fue desarrollada por un equipo de trabajo integrado por estudiantes del sistema de Técnico Superior Universitario área sistemas informáticos de la Universidad Tecnológica de Nezahualcóyotl, con la misión de brindar un servicio y un espacio de comunicación entre los estudiantes de toda la universidad, empleando las tecnologías que hoy en día son más comunes y cotidianas, eliminando las barreras de la tecnología que hasta hoy en día, es presente en la minoría de la sociedad; de igual forma tenemos la misión de actualizar nuestro sistema, estando al día con las nuevas necesidades de nuestros usuarios, para brindar un mejor servicio y obtener un crecimiento constante.</h2><br/><br/>
